export type Color = keyof typeof colors;

export function hexColor(name: Color) {
    const [r, g, b] = colors[name];
    return "#" + ((1 << 24) + (r << 16) + (g << 8) + b).toString(16).slice(1);
}

export function floatColor(name: Color): [number, number, number] {
    const color = colors[name];
    return [f32(color[0] / 255), f32(color[1] / 255), f32(color[2] / 255)];
}

export function floatColorToHex(floatColor: [number, number, number]): string {
    const r = floatColor[0] * 255;
    const g = floatColor[1] * 255;
    const b = floatColor[2] * 255;
    return "#" + ((1 << 24) + ((r | 0) << 16) + ((g | 0) << 8) + (b | 0)).toString(16).slice(1);
}

const _f32 = new Float32Array(1);
function f32(x: number): number {
    _f32[0] = x;
    return _f32[0];
}
